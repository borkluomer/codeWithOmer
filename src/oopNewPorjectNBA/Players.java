public class Players {

    //attributes
    int jerseyNumber;
    double playersHeight;
    int playersAge;
    String playerName;
    String playersLastName;
    String playersTeam;


    //Constructor

    public Players(int jerseyNumber, double playersHeight,int playersAge, String playerName,
                   String playersLastName, String playersTeam){

        this.jerseyNumber=jerseyNumber;
        this.playersHeight=playersHeight;
        this.playersLastName=playersLastName;
        this.playerName=playerName;
        this.playersTeam=playersTeam;
        this.playersAge=playersAge;




    }




}
